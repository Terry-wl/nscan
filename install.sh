# ==============================================================================

set -e

# в”Җв”Җ Colores в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
RED='\033[1;31m'
GREEN='\033[1;32m'
GRAY='\033[0;90m'
WHITE='\033[1;37m'
NC='\033[0m'

# в”Җв”Җ Banner в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
printf "\n"
printf "${RED}      в–Ҳв–Ҳв–Ҳв•—   в–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв–Ҳв•—   в–Ҳв–Ҳв•—${NC}\n"
printf "${RED}      в–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв•‘ в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қ в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қ в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв•‘${NC}\n"
printf "${RED}      в–Ҳв–Ҳв•”в–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘ в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘      в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘ в–Ҳв–Ҳв•”в–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘${NC}\n"
printf "${RED}      в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘ в•ҡв•җв•җв•җв•җв–Ҳв–Ҳв•‘ в–Ҳв–Ҳв•‘      в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•‘ в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘${NC}\n"
printf "${RED}      в–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•‘ в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘ в–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•‘${NC}\n"
printf "${RED}      в•ҡв•җв•қ  в•ҡв•җв•җв•җв•қ в•ҡв•җв•җв•җв•җв•җв•җв•қ  в•ҡв•җв•җв•җв•җв•җв•қ в•ҡв•җв•қ  в•ҡв•җв•қ в•ҡв•җв•қ  в•ҡв•җв•җв•җв•қ${NC}\n"
printf "\n"
printf "${GRAY}      в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${NC}\n"
printf "${WHITE}                       Installer${NC}\n"
printf "${GRAY}            Reconocimiento activo de infraestructura${NC}\n"
printf "${GRAY}      в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${NC}\n\n"

# в”Җв”Җ Root check в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
if [ "$(id -u)" -ne 0 ]; then
    printf "${RED}[-] Se requieren privilegios root para escribir en /usr/local/bin.${NC}\n"
    printf "    ${GRAY}Ejecuta:${NC} ${WHITE}sudo ./install.sh${NC}\n\n"
    exit 1
fi

# в”Җв”Җ Localizar el script fuente в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
SOURCE="$SCRIPT_DIR/nscan"
TARGET="/usr/local/bin/nscan"

if [ ! -f "$SOURCE" ]; then
    printf "${RED}[-] No se encontrГі '${SOURCE}'.${NC}\n"
    printf "    ${GRAY}AsegГәrate de ejecutar install.sh desde el directorio del repo.${NC}\n\n"
    exit 1
fi

# в”Җв”Җ Verificar sintaxis del script в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
printf "${GRAY}[~] Verificando sintaxis de nscan...${NC}\n"
if ! bash -n "$SOURCE" 2>/dev/null; then
    printf "${RED}[-] El script nscan tiene errores de sintaxis. Abortando.${NC}\n\n"
    exit 1
fi
printf "${GREEN}[+] Sintaxis OK.${NC}\n"

# в”Җв”Җ Instalar в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
printf "${GRAY}[~] Copiando a ${TARGET}...${NC}\n"
install -m 755 -o root -g root "$SOURCE" "$TARGET"
printf "${GREEN}[+] Instalado en ${TARGET}${NC}\n"

# в”Җв”Җ Verificar instalaciГіn в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
if ! command -v nscan >/dev/null 2>&1; then
    printf "${RED}[-] nscan no estГЎ accesible desde el PATH.${NC}\n"
    printf "    ${GRAY}Verifica que /usr/local/bin estГ© en \$PATH.${NC}\n\n"
    exit 1
fi

INSTALLED_PATH="$(command -v nscan)"
printf "${GREEN}[+] VerificaciГіn OK:${NC} ${WHITE}${INSTALLED_PATH}${NC}\n\n"

# в”Җв”Җ Mensaje final в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
printf "${WHITE}Ya puedes usar nscan:${NC}\n"
printf "    ${GRAY}sudo nscan -r <IP|CIDR>${NC}          ${GRAY}# scan bГЎsico${NC}\n"
printf "    ${GRAY}sudo nscan -r <IP> -v fast -c${NC}     ${GRAY}# rГЎpido + mostrar comandos${NC}\n"
printf "    ${GRAY}sudo nscan -h${NC}                     ${GRAY}# ayuda completa${NC}\n\n"

printf "${GRAY}La primera ejecuciГіn instalarГЎ automГЎticamente las dependencias${NC}\n"
